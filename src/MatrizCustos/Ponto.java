
public class Ponto {
	int valor;
	int posX;
	int posY;
	
	public Ponto(int _vlr, int _posX, int _posY) {
		valor = _vlr;
		posX = _posX;
		posY = _posY;
	}
	
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getPosX() {
		return posX;
	}
	public void setPosX(int posX) {
		this.posX = posX;
	}
	public int getPosY() {
		return posY;
	}
	public void setPosY(int posY) {
		this.posY = posY;
	}
	
	
	
}
