
public class ItemMatriz {
	int i;
	int j;
	float valor;
	
	public ItemMatriz(int _i, int _j, float _valor) {
		i = _i;
		j = _j;
		valor = _valor;
	}
	
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	
	
}
